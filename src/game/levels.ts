// Helper to generate a rectangular grid of active cells
const rect = (rows: number, cols: number): [number, number][] => {
  const cells: [number, number][] = [];
  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      cells.push([r, c]);
    }
  }
  return cells;
};

