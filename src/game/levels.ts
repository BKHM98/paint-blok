// Generate levels 51-200 using verified blocked cell positions
const generateLevel = (id: number): Level => {
  const difficulty = id <= 100 ? 3 : id <= 150 ? 4 : 5;
  const patternIndex = (id - 51) % GENERATED_PATTERNS.length;
  const pattern = GENERATED_PATTERNS[patternIndex];

  return {
    id,
    name: LEVEL_NAMES[(id - 51) % LEVEL_NAMES.length],
    gridRows: pattern.rows,
    gridCols: pattern.cols,
    activeCells: rect(pattern.rows, pattern.cols),
    lockedCells: [],
    blockedCells: VERIFIED_BLOCKED[id],
    requiredShapes: pattern.shapes.map(s => ({ ...s })),
    difficulty,
  };
};

// Validate a level - ensures total shape cells = paintable grid cells
const validateLevel = (level: Level): boolean => {
  const blockedCount = level.blockedCells?.length ?? 0;
  const paintableCells = level.activeCells.length - blockedCount;
  const shapeCells = level.requiredShapes.reduce((sum, item) => {
    return sum + (item.shape.pattern.length * item.required);
  }, 0);

  if (paintableCells !== shapeCells) {
    console.warn(`Level ${level.id} invalid: grid has ${paintableCells} paintable cells but shapes need ${shapeCells}`);
    return false;
  }
  return true;
};

